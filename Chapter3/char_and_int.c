// char_and_int.c
// 字符与整数的关系
#include <stdio.h>

int main()
{

   // 可以给字符类型赋值一个整数，或者以整数的形式输出字符类型
   char a = 'E';
   char b = 69;
   printf("a: %c, %d\n", a, a);
   printf("b: %c, %d\n", b, b);

   // 可以给整数类型赋值一个字符，或者以字符的形式输出整数类型
   int c = 72;
   int d = 'H';
   printf("c: %c, %d\n", c, c);
   printf("d: %c, %d\n", d, d);

   // 字符类型和整数类型之间可以直接进行运算
   printf("%c %d", d - 3, d - 3);

   return 0;

}
// 输出
// a: E, 69
// b: E, 69
// c: H, 72
// d: H, 72
// E 69
